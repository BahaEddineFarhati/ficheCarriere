package com.bct.ficheCarriere.model;

public class Formation {
	String level;
	String diploma;
	
	public Formation() {


	}

	public String getLevel() {
		return level;
	}

	public void setLevel(String level) {
		this.level = level;
	}

	public String getDiploma() {
		return diploma;
	}

	public void setDiploma(String diploma) {
		this.diploma = diploma;
	}
	
	
	
	

}
