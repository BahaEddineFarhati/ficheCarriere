package com.bct.ficheCarriere.model;

public class Mobility {
   String employeeId;
   String abroadDispo;
    String movementDispo;

    String changementDispo;

    public Mobility() {
    }

    public String getEmployeeId() {
        return employeeId;
    }

    public void setEmployeeId(String employeeId) {
        this.employeeId = employeeId;
    }

    public String getAbroadDispo() {
        return abroadDispo;
    }

    public void setAbroadDispo(String abroadDispo) {
        this.abroadDispo = abroadDispo;
    }

    public String getMovementDispo() {
        return movementDispo;
    }

    public void setMovementDispo(String movementDispo) {
        this.movementDispo = movementDispo;
    }

    public String getChangementDispo() {
        return changementDispo;
    }

    public void setChangementDispo(String changementDispo) {
        this.changementDispo = changementDispo;
    }
}
